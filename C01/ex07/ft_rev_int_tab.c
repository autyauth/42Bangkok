void	ft_rev_int_tab(int *tab, int size)
{
	int		i;
	int		j;
	char	temp[100000];

	i = 0;
	j = 0;
	while (i < size)
	{
		temp[size - i - 1] = tab[i];
		i++;
	}
	while (j < size)
	{
		tab[j] = temp[j];
		j++;
	}
}
